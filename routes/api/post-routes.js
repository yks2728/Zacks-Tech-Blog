const router = require('express').Router();
const sequelize = require('../../config/connection');
const { User, Like, Post } = require('../../models');

router.get('/', (req, res) => {
    Post.findAll({
        atrributes: ['id', 'content_data', 'title', 'created_at'],
        order: [['created_at', 'DESC']],
        include: [
            {
                model: User,
                attributes: ['username']
            }
        ]
    })
    .then(dbPostData => res.json(dbPostData))
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

router.get('/:id', (req, res) => {
    Post.findOne({
        where: {
            id: req.params.id
        },
        attributes: ['id', 'content_data', 'title', 'created_at'],
        include: [
            {
                model: User,
                attributes: ['username']
            }
        ]
    })
    .then(dbPostData => {
        if (!dbPostData) {
            res.status(404).json({ message: 'No post found with this id' });
            return;
        }
        res.json(dbPostData);
    })
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

router.post('/', (req, res) => {
    Post.create({
        title: req.body.title,
        content_data: req.body.content_data,
        user_id: req.body.user_id
    })
    .then(dbPostData => res.json(dbPostData))
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

router.put('/like', (req, res) => {
   Like.create({
       user_id: req.body.user_id,
       post_id: req.body.post_id
   }).then(() => {
       return Post.findOne({
           where: {
               id: req.body.post_id
           },
           attributes: [
               'id',
               'content_data',
               'title',
               'created_at,'
           ]
           [
               sequelize.literal('(SELECT COUNT(*) FROM like WHERE post.id = like.post_id)'),
               'like_count'
           ]
       })
       .then(dbPostData => res.json(dbPostData))
       .catch(err => {
           console.log(err);
           res.status(400).json(err);
       });
   });
});

router.put('/:id', (req, res) => {
    Post.update(
        {
            title: req.body.title
        },
        {
            where: {
                id: req.params.id
            }
        }
    )
    .then(dbPostData => {
        if (!dbPostData) {
            res.status(404).json({ message: 'No post found with this id '});
            return;
        }
        res.json(dbPostData);
    })
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

router.delete('/:id', (req, res) => {
    Post.destroy({
        where: {
            id: req.params.id
        }
    })
    .then(dbPostData => {
        if (!dbPostData) {
            res.status(404).json({ message: 'No post found with this id' });
            return;
        }
        res.json(dbPostData)
    })
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

module.exports = router;